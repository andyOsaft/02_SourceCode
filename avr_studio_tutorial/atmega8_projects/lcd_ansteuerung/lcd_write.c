//--------------------------------------------------------------------------------------------------------------
//	lcd_write(...) - sendet ein Zeichen (Daten) an das LCD
//	PE: 	text=Zeichen
//--------------------------------------------------------------------------------------------------------------
void lcd_write(char text)
{
	PORTD |= (1<<BIT2);		//RS setzen = Daten
	lcd_send(text);
}
