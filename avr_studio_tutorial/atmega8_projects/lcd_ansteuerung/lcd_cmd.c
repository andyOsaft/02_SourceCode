//--------------------------------------------------------------------------------------------------------------
//	lcd_cmd(...) - sendet ein Kommando an LCD
//	PE:	cmd= Kommando-Byte
//--------------------------------------------------------------------------------------------------------------
void lcd_cmd(char cmd)
{
	PORTD &= ~(1<<BIT2);	//RS löschen = Kommando folgt
	lcd_send(cmd);			//senden;
}
